  Future loadData() async {
    print(http.token);
    var response = await http.get("/Video/GetAllVideo?pageIndex=0&pageSize=10");
    // var response =await http.get("/Chat/GetallUserName?pageIndex=0&pageSize=10");
    print(response);
    if (response.data['state'] == 200) {
      final data = response.data["data"];
      if (data != null) {
        VideoList = VideoListData.fromJsonList(data['videos']);
        setState(() {});
      }
      print(VideoList[0]);
    } else {
      // 处理错误情况
    }
  }

